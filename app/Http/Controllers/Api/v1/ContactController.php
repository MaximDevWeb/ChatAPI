    /**
     * Store a newly created resource in storage.
     *
     * @param Request $request
     * @return JsonResponse
     */
    public function store(Request $request): JsonResponse
    {
        $request->validate([
            'contact_id' => 'integer'
        ]);

        Auth::user()->contacts()->create($request->all());

        broadcast(
            new ContactCreated(
                Auth::user()->login,
                $request->get('contact_id')
            )
        )->toOthers();

        return response()->json([
            'status' => 'success',
        ]);
    }

